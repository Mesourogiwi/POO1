    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (((String)CbCliente.getSelectedItem()).equals("")){
          JOptionPane.showMessageDialog(null, "Selecione um cliente para salvar");
         CbCliente.requestFocus();
     }
     else if (((String)CbProduto.getSelectedItem()).equals("")){
          JOptionPane.showMessageDialog(null, "Selecione um produto para salvar");
         CbCliente.requestFocus();
     }
       else if (TxtNumero.getText() == null  ||TxtNumero.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite a quantidade antes de salvar!!");
            TxtNumero.requestFocus();
        }
      
       
       else{
           
        nome = (String) CbCliente.getSelectedItem();
        for( c = 0; c<i;c++){
           
           if(nome ==(String) cl[c].getNome()){
                break;
           }
       }
        prod = (String) CbProduto.getSelectedItem();
       if(chNacional.isSelected()){ 
             for( contp = 0; contp<j;contp++){
           
           if(prod ==(String) prn[contp].getDescricao()){
                if(cheque!= null){
                 tipopgto="cheque";
                  num = TxtNumero.getText();
                 Cheque  ch = new Cheque(tipopgto,nome,cheque);
                 total =prn[contp].calcularPreco();
                  venda[l] = new Venda( num, cl[c],prn[contp],ch,l,total);
                  JOptionPane.showMessageDialog(null, "Salvo com sucesso!!");
                  l++;
                }
                else if(cartao!= null){
                  tipopgto = "cartao";
                   num = TxtNumero.getText();
                  Cartao  ct = new Cartao(tipopgto,nome,cartao);
                  total =prn[contp].calcularPreco();
                  venda[l] = new Venda(num, cl[c],prn[contp],ct,l,total);
                  JOptionPane.showMessageDialog(null, "Salvo com sucesso!!");
                  l++;
                }
                else{
                    JOptionPane.showMessageDialog(null, "Erro no pagamento!!");
                }
                break;
                     }
                 }
            }
      
       else if(chImportado.isSelected()){
            for( contp = 0; contp<k;contp++){
           
           if(prod ==(String) pri[contp].getDescricao()){
                   if(cheque!= null){
                 tipopgto="cheque";
                 num = TxtNumero.getText();
                 Cheque  ch = new Cheque(tipopgto,nome,cheque);
                  total =pri[contp].calcularPreco();
                  venda[l] = new Venda( num, cl[c],pri[contp],ch,l,total);
                  JOptionPane.showMessageDialog(null, "Salvo com sucesso!!");
                  l++;
                }
                else if(cartao!= null){
                  tipopgto = "cartao";
                   num = TxtNumero.getText();
                  Cartao  ct = new Cartao(tipopgto,nome,cartao);
                  total =pri[contp].calcularPreco();
                  venda[l] = new Venda(num, cl[c],pri[contp],ct,l,total);
                  l++;
                  JOptionPane.showMessageDialog(null, "Salvo com sucesso!!");
                }
                else if(dinheiro!= null){
                  tipopgto = "dinheiro";
                   num = TxtNumero.getText();
                  Dinheiro  din = new Dinheiro(tipopgto);
                  total =prn[contp].calcularPreco();
                  venda[l] = new Venda(num, cl[c],pri[contp],din,l,total);
                  l++;
                  JOptionPane.showMessageDialog(null, "Salvo com sucesso!!");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Erro no pagamento!!");
                }
                break;
           }
           else{
               JOptionPane.showMessageDialog(null, "Erro na descricao do produto!!");
           }
          
       }
       
       
       
       }
        
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

